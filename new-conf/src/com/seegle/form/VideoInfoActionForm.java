package com.seegle.form;

/**
 *
 * 录像文件信息，对应file_info表
 */

public class VideoInfoActionForm extends org.apache.struts.action.ActionForm {
    
    private int id;  //索引
    private String file_real_name;
    private String file_hash_name;
    private String file_text;
    private String file_time;
    private String file_size;
    private int file_view_power;
    private int file_flag;
    private int file_down_power;
    private int file_categories;
    private String download_url;
    private String del_url;
    private int pageNumber = 0;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFile_real_name() {
		return file_real_name;
	}
	public void setFile_real_name(String file_real_name) {
		this.file_real_name = file_real_name;
	}
	public String getFile_hash_name() {
		return file_hash_name;
	}
	public void setFile_hash_name(String file_hash_name) {
		this.file_hash_name = file_hash_name;
	}
	public String getFile_text() {
		return file_text;
	}
	public void setFile_text(String file_text) {
		this.file_text = file_text;
	}
	public String getFile_time() {
		return file_time;
	}
	public void setFile_time(String file_time) {
		this.file_time = file_time;
	}
	public String getFile_size() {
		return file_size;
	}
	public void setFile_size(String file_size) {
		this.file_size = file_size;
	}
	public int getFile_view_power() {
		return file_view_power;
	}
	public void setFile_view_power(int file_view_power) {
		this.file_view_power = file_view_power;
	}
	public int getFile_flag() {
		return file_flag;
	}
	public void setFile_flag(int file_flag) {
		this.file_flag = file_flag;
	}
	public int getFile_down_power() {
		return file_down_power;
	}
	public void setFile_down_power(int file_down_power) {
		this.file_down_power = file_down_power;
	}
	public int getFile_categories() {
		return file_categories;
	}
	public void setFile_categories(int file_categories) {
		this.file_categories = file_categories;
	}
	public String getDownload_url() {
		return download_url;
	}
	public void setDownload_url(String download_url) {
		this.download_url = download_url;
	}
	public String getDel_url() {
		return del_url;
	}
	public void setDel_url(String del_url) {
		this.del_url = del_url;
	}
	public int getPageNumber() {
		return pageNumber;
	}
	public void setPageNumber(int pageNumber) {
		this.pageNumber = pageNumber;
	}
}
