package com.seegle.form;

public class MeetingCountUserActionForm extends org.apache.struts.action.ActionForm {

	private String useraccount;
	private int userid;
	private String username;
	private String usertotaltime;
	private String useronlinenum;
	private String avglogintime;
	private String logintime;
	
	public String getUseraccount() {
		return useraccount;
	}
	public void setUseraccount(String useraccount) {
		this.useraccount = useraccount;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUsertotaltime() {
		return usertotaltime;
	}
	public void setUsertotaltime(String usertotaltime) {
		this.usertotaltime = usertotaltime;
	}
	public String getUseronlinenum() {
		return useronlinenum;
	}
	public void setUseronlinenum(String useronlinenum) {
		this.useronlinenum = useronlinenum;
	}
	public String getAvglogintime() {
		return avglogintime;
	}
	public void setAvglogintime(String avglogintime) {
		this.avglogintime = avglogintime;
	}
	public String getLogintime() {
		return logintime;
	}
	public void setLogintime(String logintime) {
		this.logintime = logintime;
	}
	
}
