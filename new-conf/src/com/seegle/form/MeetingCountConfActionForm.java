package com.seegle.form;

public class MeetingCountConfActionForm extends org.apache.struts.action.ActionForm {

	private int confid;
	private String confname;
	private int usernum;
	private int attendance;
	private String usertotaltime;
	private int conftotalnum;
	private String conftotaltime;
	public int getConfid() {
		return confid;
	}
	public void setConfid(int confid) {
		this.confid = confid;
	}
	public String getConfname() {
		return confname;
	}
	public void setConfname(String confname) {
		this.confname = confname;
	}
	public int getUsernum() {
		return usernum;
	}
	public void setUsernum(int usernum) {
		this.usernum = usernum;
	}
	public int getAttendance() {
		return attendance;
	}
	public void setAttendance(int attendance) {
		this.attendance = attendance;
	}
	public String getUsertotaltime() {
		return usertotaltime;
	}
	public void setUsertotaltime(String usertotaltime) {
		this.usertotaltime = usertotaltime;
	}
	public int getConftotalnum() {
		return conftotalnum;
	}
	public void setConftotalnum(int conftotalnum) {
		this.conftotalnum = conftotalnum;
	}
	public String getConftotaltime() {
		return conftotaltime;
	}
	public void setConftotaltime(String conftotaltime) {
		this.conftotaltime = conftotaltime;
	}
		   
}
