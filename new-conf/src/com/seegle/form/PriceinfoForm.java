package com.seegle.form;

import java.io.Serializable;

/**
 * 单价类，对应单价表
 * @author zhangqing
 * @date 2014-7-23 上午11:13:40
 */
public class PriceinfoForm implements Serializable{
	private String id;
	private String orgid;//企业ID
	private String price;//单价
	private String allowoverfulfil;//是否允许超额
	private String overfulfilprice;//超额单价
	private String begintime;//计费生效时间
	private String operateuser;//操作员
	private String param;//备用字段
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getOrgid() {
		return orgid;
	}
	public void setOrgid(String orgid) {
		this.orgid = orgid;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getAllowoverfulfil() {
		return allowoverfulfil;
	}
	public void setAllowoverfulfil(String allowoverfulfil) {
		this.allowoverfulfil = allowoverfulfil;
	}
	public String getOverfulfilprice() {
		return overfulfilprice;
	}
	public void setOverfulfilprice(String overfulfilprice) {
		this.overfulfilprice = overfulfilprice;
	}
	public String getBegintime() {
		return begintime;
	}
	public void setBegintime(String begintime) {
		this.begintime = begintime;
	}
	public String getOperateuser() {
		return operateuser;
	}
	public void setOperateuser(String operateuser) {
		this.operateuser = operateuser;
	}
	public String getParam() {
		return param;
	}
	public void setParam(String param) {
		this.param = param;
	}
}
