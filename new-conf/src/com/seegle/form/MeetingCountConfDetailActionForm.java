package com.seegle.form;

public class MeetingCountConfDetailActionForm extends org.apache.struts.action.ActionForm {

	private String useraccount;
	private int userid;
	private String username;
	private String userlogintime;
	private String userlogouttime;
	private String useronlinetime;
	
	public String getUseraccount() {
		return useraccount;
	}
	public void setUseraccount(String useraccount) {
		this.useraccount = useraccount;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserlogintime() {
		return userlogintime;
	}
	public void setUserlogintime(String userlogintime) {
		this.userlogintime = userlogintime;
	}
	public String getUserlogouttime() {
		return userlogouttime;
	}
	public void setUserlogouttime(String userlogouttime) {
		this.userlogouttime = userlogouttime;
	}
	public String getUseronlinetime() {
		return useronlinetime;
	}
	public void setUseronlinetime(String useronlinetime) {
		this.useronlinetime = useronlinetime;
	}
	
}
