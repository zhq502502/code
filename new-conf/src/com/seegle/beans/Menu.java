package com.seegle.beans;

/**
 * 菜单项实体类
 * @author zhangqing
 * @date 2013-7-10 下午01:54:42
 */
public class Menu {
	/**id*/
	private String id;
	/**父id*/
	private String parentId;
	/**名称*/
	private String menuName;
	/**Url*/
	private String menuUrl;
	/**排序*/
	private String menuIndex;
	/**权限代码*/
	private String menuCode;
	/**描述*/
	private String menuDiscription;
	/**标识*/
	private String flag;
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getParentId() {
		return parentId;
	}
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuUrl() {
		return menuUrl;
	}
	public void setMenuUrl(String menuUrl) {
		this.menuUrl = menuUrl;
	}
	public String getMenuIndex() {
		return menuIndex;
	}
	public void setMenuIndex(String menuIndex) {
		this.menuIndex = menuIndex;
	}
	public String getMenuCode() {
		return menuCode;
	}
	public void setMenuCode(String menuCode) {
		this.menuCode = menuCode;
	}
	public String getMenuDiscription() {
		return menuDiscription;
	}
	public void setMenuDiscription(String menuDiscription) {
		this.menuDiscription = menuDiscription;
	}
	
}
