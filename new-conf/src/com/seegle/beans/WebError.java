package com.seegle.beans;
/**
 * web出错对象封装
 * @author zhangqing
 * @date 2014-3-28 上午10:12:49
 */
public class WebError {
	private Integer code;//错误代码
	private Integer exception;//是否异常，0有，1无
	private String stack;//错误栈信息
	private String errormsg;//错误信息
	private String errorservlet;//错误servlet
	private String errorurl;//错误url
	public Integer getCode() {
		return code;
	}
	public void setCode(Integer code) {
		this.code = code;
	}
	public Integer getException() {
		return exception;
	}
	public void setException(Integer exception) {
		this.exception = exception;
	}
	public String getStack() {
		return stack;
	}
	public void setStack(String stack) {
		this.stack = stack;
	}
	public String getErrormsg() {
		return errormsg;
	}
	public void setErrormsg(String errormsg) {
		this.errormsg = errormsg;
	}
	public String getErrorservlet() {
		return errorservlet;
	}
	public void setErrorservlet(String errorservlet) {
		this.errorservlet = errorservlet;
	}
	public String getErrorurl() {
		return errorurl;
	}
	public void setErrorurl(String errorurl) {
		this.errorurl = errorurl;
	}
}
